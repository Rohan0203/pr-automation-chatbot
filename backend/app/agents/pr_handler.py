"""

PR Handler - Manages the multi-step PR setup phase.

Flow:  ask_branch -> ask_title -> show_files -> confirm -> execute

At each step the user can:
  - answer the question
  - say add resource / I want another resource  -> handled by generator_agent
  - say cancel / skip pr                        -> abort PR flow
"""
