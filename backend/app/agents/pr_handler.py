def build_response(
    message,
    resource_type=None,
    resource_status=None,
    generated_yaml=None,
    needs_confirmation=False,
    pr_url=None,
    _extracted_fields=None,
):
    resp = {
        "message": message,
        "resource_type": resource_type,
        "resource_status": resource_status,
        "generated_yaml": generated_yaml,
        "needs_confirmation": needs_confirmation,
        "pr_url": pr_url,
    }
    if _extracted_fields is not None:
        resp["_extracted_fields"] = _extracted_fields
    return resp


async def _save_pr_record(
    session,
    pr_url,
    branch_name,
    target_branch,
    title,
    resource_types,
    resource_count=1,
):
    try:
        from app.models.database import async_session_factory
        from app.models.schemas import PRRecord

        pr_number = None
        match = re.search(r"/pull/(\d+)", pr_url)
        if match:
            pr_number = int(match.group(1))

        repo_full_name = (
            f"{settings.github_upstream_owner}/{settings.github_upstream_repo}"
        )

        async with async_session_factory() as db:
            record = PRRecord(
                session_id=session.session_id,
                github_username=session.github_username or "unknown",
                pr_url=pr_url,
                pr_number=pr_number,
                repo_full_name=repo_full_name,
                branch_name=branch_name,
                target_branch=target_branch or settings.github_upstream_branch,
                resource_types=resource_types,
                resource_count=resource_count,
                title=title,
            )
            db.add(record)
            await db.commit()
            logger.info(f"Saved PR record: {pr_url}")
    except Exception as e:
        logger.error(f"Failed to save PR record: {e}")


def _generate_smart_title(agent):
    rtype = (agent.resource_type or "unknown").upper()
    fields = agent.collected_fields or {}
    intake_id = fields.get("intake_id", "unknown")
    resource_name = (
        fields.get("bucket_name")
        or fields.get("database_name")
        or fields.get("role_name")
        or "unknown"
    )
    return f"[CONFIG] {rtype} - {intake_id} - {resource_name}"


def _generate_smart_body(agent):
    rtype = (agent.resource_type or "unknown").upper()
    fields = agent.collected_fields or {}
    intake_id = fields.get("intake_id", "unknown")
    resource_name = (
        fields.get("bucket_name")
        or fields.get("database_name")
        or fields.get("role_name")
        or "unknown"
    )
    yaml_content = agent.generated_yaml or "(no YAML)"
    return (
        f"## Infrastructure Configuration\n\n"
        f"**Resource Type:** {rtype}\n"
        f"**Intake ID:** {intake_id}\n"
        f"**Resource Name:** {resource_name}\n\n"
        f"```yaml\n{yaml_content}\n```\n\n"
        f"---\n"
        f"_This PR was automatically generated by PR Chatbot._"
    )


def _resolve_file_paths(batch):
    from app.services.path_resolver import resolve_file_path

    paths = []
    for entry in batch:
        try:
            path = resolve_file_path(
                entry["resource_type"], entry.get("fields", {})
            )
        except Exception:
            name = entry.get("resource_name") or entry.get("intake_id", "unknown")
            path = f"configs/{entry['resource_type']}/{entry.get('intake_id', '')}-{name}.yaml"
        paths.append(path)
    return paths


def _is_skip(msg):
    return msg in {"skip", "skip pr", "no pr", "save only", "3"}


def _is_cancel(msg):
    return msg in {"cancel", "abort", "stop", "quit"}


def _wants_new_resource(msg):
    triggers = [
        "add another",
        "add resource",
        "add a resource",
        "new resource",
        "i want another",
        "create another",
        "one more",
        "add one more",
        "i want to create",
        "i need another",
    ]
    return any(t in msg for t in triggers)


