def resume_pr_from_pause(session):
    paused = session.paused_pr_state
    if not paused:
        return None

    agent = session.start_new_resource()
    agent.resource_type = paused["resource_type"]
    agent.phase = "pr_setup"
    agent.pr_sub_phase = paused["pr_sub_phase"]
    agent.pr_branch_name = paused["pr_branch_name"]
    agent.pr_title = paused["pr_title"]
    agent.pr_body = paused["pr_body"]
    agent.pr_target_branch = paused["pr_target_branch"]
    agent.available_branches = paused["available_branches"]
    agent.fork_exists = paused["fork_exists"]
    agent.fork_full_name = paused["fork_full_name"]
    agent.collected_fields = paused["collected_fields"]
    agent.generated_yaml = paused["generated_yaml"]

    session.paused_pr_state = None

    is_batch = agent.resource_type == "batch"
    batch = session.batch

    if is_batch and batch:
        from collections import Counter

        count = len(batch)
        type_counts = Counter(e["resource_type"].upper() for e in batch)
        type_summary = ", ".join(f"{v} {k}" for k, v in type_counts.items())
        agent.pr_title = f"[CONFIG] Batch - {type_summary}"
        agent.collected_fields["batch_count"] = count
        agent.collected_fields["type_summary"] = type_summary
        body_lines = [
            f"## Infrastructure Configuration Batch ({count} resources)\n"
        ]
        for i, entry in enumerate(batch, 1):
            body_lines.append(
                f"### {i}. {entry['resource_type'].upper()} - {entry['resource_name']}"
            )
            body_lines.append(f"**Intake ID:** {entry['intake_id']}")
            body_lines.append(f"```yaml\n{entry['yaml']}\n```\n")
        body_lines.append(
            "---\n_This PR was automatically generated by PR Chatbot._"
        )
        agent.pr_body = "\n".join(body_lines)
        agent.generated_yaml = "\n---\n".join(e["yaml"] for e in batch)

    batch_count = len(batch) if batch else 0

    if is_batch:
        agent.pr_sub_phase = "show_files"
        msg = (
            f"**PR setup resumed!** Your batch now has"
            f" {batch_count} resource(s).\n\n"
            f"Target branch: `{agent.pr_target_branch}`"
            f" | Title: **{agent.pr_title}**\n\n"
        )
        files_response = _present_files(session, agent)
        files_response["message"] = msg + files_response["message"]
        return files_response
    else:
        sub = agent.pr_sub_phase
        msg = "**PR setup resumed** (continuing from where you left off).\n\n"

        if sub == "ask_branch":
            branch_list = "\n".join(
                f"  - `{b}`" for b in (agent.available_branches or [])[:10]
            )
            return build_response(
                msg + f"Which branch should this PR target?\n{branch_list}"
                f"\n\n**confirm** to use `{agent.pr_target_branch}`.",
                resource_type=agent.resource_type,
                resource_status="pr_setup",
            )
        elif sub == "ask_title":
            return build_response(
                msg + f"Current title: **{agent.pr_title}**\n\n"
                f"**confirm** to keep, or type `title: New Title` to change.",
                resource_type=agent.resource_type,
                resource_status="pr_setup",
            )
        elif sub == "show_files":
            files_response = _present_files(session, agent)
            files_response["message"] = msg + files_response["message"]
            return files_response
        elif sub == "confirm":
            summary = _present_final_summary(session, agent)
            summary["message"] = msg + summary["message"]
            return summary
        else:
            return build_response(
                msg + "Say **confirm** to continue or **cancel** to abort.",
                resource_type=agent.resource_type,
                resource_status="pr_setup",
            )
