def _handle_skip(session, agent):
    agent.phase = "done"
    rtype = (agent.resource_type or "unknown").upper()

    if agent.resource_type == "batch":
        for entry in session.batch:
            session.completed_resources.append(entry)
        session.clear_batch()
        session.current_agent = None
        count = len(session.completed_resources)
        return build_response(
            f"**{count} configurations saved!** (No PR created)\n\n"
            f"Would you like to create another resource, or are we done?",
            resource_status="confirmed",
        )

    session.complete_current_resource()
    return build_response(
        f"**{rtype} configuration saved!** (No PR created)\n\n"
        f"Would you like to create another resource, or are we done?",
        resource_type=agent.resource_type,
        resource_status="confirmed",
        generated_yaml=agent.generated_yaml,
    )


def _handle_pause_for_new_resource(session, agent):
    session.paused_pr_state = {
        "pr_sub_phase": getattr(agent, "pr_sub_phase", "ask_branch"),
        "pr_branch_name": agent.pr_branch_name,
        "pr_title": agent.pr_title,
        "pr_body": agent.pr_body,
        "pr_target_branch": agent.pr_target_branch,
        "available_branches": agent.available_branches,
        "fork_exists": agent.fork_exists,
        "fork_full_name": agent.fork_full_name,
        "resource_type": agent.resource_type,
        "collected_fields": (agent.collected_fields or {}).copy(),
        "generated_yaml": agent.generated_yaml,
    }

    session.current_agent = None

    batch_count = len(session.batch)
    return build_response(
        f"PR setup paused. Your batch"
        f" ({batch_count} resource{'s' if batch_count != 1 else ''})"
        f" is safe.\n\nGo ahead - what resource would you like to add?",
        resource_status="idle",
    )


async def _handle_llm_fallback(session, agent, user_message):
    upstream = f"{settings.github_upstream_owner}/{settings.github_upstream_repo}"

    if agent.fork_exists:
        fork_info = f"`{agent.fork_full_name}` (exists)"
    else:
        fork_info = (
            f"`{session.github_username or 'you'}"
            f"/{settings.github_upstream_repo}` (will be created)"
        )

    prompt = PR_SETUP_PROMPT.format(
        resource_type=agent.resource_type or "unknown",
        branch_name=agent.pr_branch_name or "N/A",
        fork_info=fork_info,
        upstream_info=(
            f"{upstream}:"
            f"{agent.pr_target_branch or settings.github_upstream_branch}"
        ),
        target_branch=agent.pr_target_branch or settings.github_upstream_branch,
        available_branches=", ".join(
            agent.available_branches or [settings.github_upstream_branch]
        ),
        pr_title=agent.pr_title or "(auto-generated)",
        yaml_content=agent.generated_yaml or "(no YAML generated yet)",
        user_message=user_message,
    )

    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": prompt},
    ]

    try:
        result = await llm_client.extract_json(messages)
    except Exception as e:
        logger.error(f"PR setup LLM fallback failed: {e}")
        return build_response(
            "I had trouble understanding that. You can:\n"
            "  - **confirm** / **yes** - proceed\n"
            "  - **cancel** / **skip pr** - abort\n"
            "  - Type `title: X` to change the title",
            resource_type=agent.resource_type,
            resource_status="pr_setup",
        )

    action = result.get("action", "question")
    message = result.get("message", "")
    proceed_after = result.get("proceed_after", False)

    if action == "edit_config":
        extracted_fields = result.get("extracted_fields", {})
        if extracted_fields and isinstance(extracted_fields, dict):
            return build_response(
                message or "Updating your configuration...",
                resource_type=agent.resource_type,
                resource_status="edit_config_requested",
                generated_yaml=agent.generated_yaml,
                _extracted_fields=extracted_fields,
            )
        return build_response(
            "Which field would you like to change?",
            resource_type=agent.resource_type,
            resource_status="pr_setup",
        )

    if action == "show_yaml":
        yaml_display = agent.generated_yaml or "(No YAML generated yet)"
        return build_response(
            f"```yaml\n{yaml_display}\n```\n\n"
            f"You can **confirm** to continue, or **edit** fields.",
            resource_type=agent.resource_type,
            resource_status="pr_setup",
            generated_yaml=agent.generated_yaml,
        )

    if action == "change_settings":
        changes = []
        new_branch = result.get("branch_name")
        if new_branch:
            agent.pr_branch_name = new_branch.strip().strip("`'\"")
            changes.append(f"Branch: `{agent.pr_branch_name}`")

        new_target = result.get("target_branch")
        if new_target:
            new_target = new_target.strip().strip("`'\"")
            available = agent.available_branches or []
            if available and new_target not in available:
                branch_list = ", ".join(f"`{b}`" for b in available)
                return build_response(
                    f"Branch `{new_target}` doesn't exist.\n"
                    f"Available: {branch_list}",
                    resource_type=agent.resource_type,
                    resource_status="pr_setup",
                )
            agent.pr_target_branch = new_target
            changes.append(f"Target: `{new_target}`")

        new_title = result.get("pr_title")
        if new_title:
            agent.pr_title = new_title.strip()
            changes.append(f"Title: **{agent.pr_title}**")

        new_body = result.get("pr_body")
        if new_body:
            agent.pr_body = new_body.strip()
            changes.append("Description updated")

        if proceed_after:
            if agent.resource_type == "batch":
                return await execute_batch_pr_creation(session, agent)
            return await execute_pr_creation(session, agent)

        if changes:
            summary = "\n".join(f"- {c}" for c in changes)
            return build_response(
                f"Updated:\n{summary}\n\nSay **confirm** to continue.",
                resource_type=agent.resource_type,
                resource_status="pr_setup",
            )

    if action == "proceed":
        if agent.resource_type == "batch":
            return await execute_batch_pr_creation(session, agent)
        return await execute_pr_creation(session, agent)

    if action == "skip":
        return _handle_skip(session, agent)

    return build_response(
        message or "Say **confirm** to continue,"
        " or tell me what you would like to change.",
        resource_type=agent.resource_type,
        resource_status="pr_setup",
    )


# ================================================================
# PR EXECUTION
# ================================================================


async def execute_pr_creation(session, agent):
    rtype = agent.resource_type or "unknown"
    fields = agent.collected_fields or {}
    resource_name = (
        fields.get("bucket_name")
        or fields.get("database_name")
        or fields.get("role_name")
        or "unknown"
    )
    intake_id = fields.get("intake_id", "unknown")

    try:
        pr_result = await create_pr_for_resource(
            github_token=session.github_token,
            github_username=session.github_username or "unknown",
            resource_type=rtype,
            intake_id=intake_id,
            resource_name=resource_name,
            yaml_content=agent.generated_yaml,
            branch_name=agent.pr_branch_name,
            pr_title=agent.pr_title,
            pr_body=agent.pr_body,
            collected_fields=fields,
            upstream_branch=agent.pr_target_branch,
        )
    except Exception as e:
        pr_result = {"success": False, "error": str(e)}

    if pr_result and pr_result.get("success"):
        agent.phase = "done"
        agent._pr_url = pr_result["pr_url"]

        await _save_pr_record(
            session=session,
            pr_url=pr_result["pr_url"],
            branch_name=pr_result.get("branch_name", agent.pr_branch_name),
            target_branch=agent.pr_target_branch,
            title=pr_result.get("title", agent.pr_title),
            resource_types=[rtype],
            resource_count=1,
        )

        session.complete_current_resource()
        return build_response(
            f"**{rtype.upper()} - PR created successfully!**\n\n"
            f"- Branch: `{pr_result.get('branch_name', 'N/A')}`\n"
            f"- File: `{pr_result.get('file_path', 'N/A')}`\n"
            f"- PR: {pr_result['pr_url']}\n\n"
            f"Would you like to create another resource, or are we done?",
            resource_type=agent.resource_type,
            resource_status="confirmed",
            generated_yaml=agent.generated_yaml,
            pr_url=pr_result["pr_url"],
        )
    else:
        error_msg = (
            pr_result.get("error", "Unknown error") if pr_result else "Unknown error"
        )
        return build_response(
            f"PR creation failed: {error_msg}\n\n"
            f"You can:\n"
            f"  - **retry** - try again\n"
            f"  - **skip pr** - save without PR\n"
            f"  - **cancel** - discard",
            resource_type=agent.resource_type,
            resource_status="pr_setup",
            generated_yaml=agent.generated_yaml,
        )


async def execute_batch_pr_creation(session, agent):
    batch = session.batch
    if not batch:
        return build_response("No resources in batch.", resource_status="confirmed")

    count = len(batch)
    type_summary = agent.collected_fields.get("type_summary", f"{count} resources")

    resources = []
    for entry in batch:
        r = {
            "resource_type": entry["resource_type"],
            "intake_id": entry["intake_id"],
            "resource_name": entry["resource_name"],
            "yaml_content": entry["yaml"],
        }
        try:
            from app.services.path_resolver import resolve_file_path

            r["resolved_path"] = resolve_file_path(
                entry["resource_type"], entry.get("fields", {})
            )
        except Exception:
            pass
        resources.append(r)

    pr_title = agent.pr_title or f"[CONFIG] Batch - {type_summary}"
    pr_body = agent.pr_body

    if not pr_body:
        body_lines = ["## Infrastructure Configuration Batch\n"]
        for i, entry in enumerate(batch, 1):
            body_lines.append(
                f"### {i}. {entry['resource_type'].upper()} - {entry['resource_name']}"
            )
            body_lines.append(f"**Intake ID:** {entry['intake_id']}")
            body_lines.append(f"```yaml\n{entry['yaml']}\n```\n")
        body_lines.append(
            "---\n_This PR was automatically generated by PR Chatbot._"
        )
        pr_body = "\n".join(body_lines)

    try:
        pr_result = await create_pr_for_batch(
            github_token=session.github_token,
            github_username=session.github_username or "unknown",
            resources=resources,
            branch_name=agent.pr_branch_name,
            pr_title=pr_title,
            pr_body=pr_body,
            upstream_branch=agent.pr_target_branch,
        )
    except Exception as e:
        pr_result = {"success": False, "error": str(e)}

    if pr_result and pr_result.get("success"):
        batch_types = list(set(e["resource_type"] for e in batch))
        await _save_pr_record(
            session=session,
            pr_url=pr_result["pr_url"],
            branch_name=pr_result.get("branch_name", agent.pr_branch_name),
            target_branch=agent.pr_target_branch,
            title=pr_result.get("title", pr_title),
            resource_types=batch_types,
            resource_count=count,
        )

        for entry in batch:
            entry["pr_url"] = pr_result["pr_url"]
            session.completed_resources.append(entry)
        session.clear_batch()
        session.current_agent = None

        file_count = len(pr_result.get("file_paths", resources))
        return build_response(
            f"**PR created with {count} resources!**\n\n"
            f"- Branch: `{pr_result.get('branch_name', 'N/A')}`\n"
            f"- Files: {file_count}\n"
            f"- PR: {pr_result['pr_url']}\n\n"
            f"Would you like to create more resources, or are we done?",
            resource_status="confirmed",
            pr_url=pr_result["pr_url"],
        )
    else:
        error_msg = (
            pr_result.get("error", "Unknown error") if pr_result else "Unknown error"
        )
        return build_response(
            f"PR creation failed: {error_msg}\n\n"
            f"You can:\n"
            f"  - **retry** - try again\n"
            f"  - **skip pr** - save without PR\n"
            f"  - **cancel** - discard",
            resource_status="pr_setup",
        )


