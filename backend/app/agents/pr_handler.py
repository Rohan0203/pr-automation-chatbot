def _handle_ask_branch(session, agent, user_message):
    msg_lower = user_message.strip().lower()
    confirm_words = {"confirm", "yes", "ok", "default", "y", "proceed", "1"}

    if msg_lower not in confirm_words:
        branch = user_message.strip().strip("`'\"")
        available = agent.available_branches or []
        if available and branch not in available:
            branch_list = "\n".join(f"  - `{b}`" for b in available[:10])
            return build_response(
                f"Branch `{branch}` doesn't exist on the upstream repo.\n\n"
                f"Available branches:\n{branch_list}\n\n"
                f"Pick one of these, or **confirm** to use"
                f" `{agent.pr_target_branch}`.",
                resource_type=agent.resource_type,
                resource_status="pr_setup",
            )
        agent.pr_target_branch = branch

    agent.pr_sub_phase = "ask_title"

    suggested_title = agent.pr_title or "(auto-generated)"
    suggested_body = agent.pr_body or "(auto-generated)"
    body_preview = suggested_body[:300]
    if len(suggested_body) > 300:
        body_preview += "\n..."

    msg = (
        f"Target branch: `{agent.pr_target_branch}`\n\n"
        f"**Suggested PR title:**\n> {suggested_title}\n\n"
        f"**Suggested PR body:**\n```\n{body_preview}\n```\n\n"
        f"Would you like to **confirm** these, or provide your own?\n"
        f"Examples:\n"
        f"  - `confirm` - use the suggested title and body\n"
        f"  - `title: My Custom Title` - change only the title\n"
        f"  - `body: My custom description` - change only the body\n"
        f"  - provide both: `title: X` and `body: Y`"
    )
    return build_response(
        msg, resource_type=agent.resource_type, resource_status="pr_setup"
    )


async def _handle_ask_title(session, agent, user_message):
    msg_lower = user_message.strip().lower()
    confirm_words = {
        "confirm", "yes", "ok", "y", "looks good", "proceed", "1", "lgtm"
    }

    if msg_lower not in confirm_words:
        title_match = re.search(
            r"title\s*[:=]\s*(.+?)(?:\n|body\s*[:=]|$)",
            user_message,
            re.IGNORECASE,
        )
        body_match = re.search(
            r"body\s*[:=]\s*(.+)",
            user_message,
            re.IGNORECASE | re.DOTALL,
        )

        if title_match:
            agent.pr_title = title_match.group(1).strip().strip("`'\"")
        elif not body_match:
            return await _handle_llm_fallback(session, agent, user_message)

        if body_match:
            agent.pr_body = body_match.group(1).strip().strip("`'\"")

    agent.pr_sub_phase = "show_files"
    return _present_files(session, agent)


def _present_files(session, agent):
    is_batch = agent.resource_type == "batch"
    batch = session.batch

    if is_batch and batch:
        paths = _resolve_file_paths(batch)
        file_lines = "\n".join(f"  {i}. `{p}`" for i, p in enumerate(paths, 1))
    else:
        try:
            from app.services.path_resolver import resolve_file_path

            path = resolve_file_path(
                agent.resource_type, agent.collected_fields or {}
            )
        except Exception:
            path = f"configs/{agent.resource_type}/unknown.yaml"
        file_lines = f"  1. `{path}`"

    msg = (
        f"**Files to be created:**\n{file_lines}\n\n"
        f"Does this look correct?\n"
        f"  - **confirm** - proceed to final summary\n"
        f"  - Tell me what is wrong and I will adjust"
    )
    return build_response(
        msg, resource_type=agent.resource_type, resource_status="pr_setup"
    )


def _handle_show_files(session, agent, user_message):
    msg_lower = user_message.strip().lower()
    confirm_words = {
        "confirm", "yes", "ok", "y", "looks good", "proceed",
        "correct", "1", "lgtm", "good",
    }

    if msg_lower in confirm_words:
        agent.pr_sub_phase = "confirm"
        return _present_final_summary(session, agent)

    return build_response(
        "File paths are automatically determined from your resource configuration"
        " (account ID to folder mapping). To change a file path, you would need"
        " to edit the resource's `aws_account_id` or `bucket_name`.\n\n"
        "Would you like to **confirm** these paths, or **cancel** to go back?",
        resource_type=agent.resource_type,
        resource_status="pr_setup",
    )


def _present_final_summary(session, agent):
    is_batch = agent.resource_type == "batch"
    batch = session.batch
    upstream = f"{settings.github_upstream_owner}/{settings.github_upstream_repo}"

    if agent.fork_exists:
        fork_line = f"`{agent.fork_full_name}` (exists)"
    else:
        fork_line = (
            f"`{session.github_username or 'you'}"
            f"/{settings.github_upstream_repo}` (will be created)"
        )

    if is_batch and batch:
        paths = _resolve_file_paths(batch)
        file_lines = "\n".join(f"  - `{p}`" for p in paths)
        resource_count = len(batch)
    else:
        try:
            from app.services.path_resolver import resolve_file_path

            path = resolve_file_path(
                agent.resource_type, agent.collected_fields or {}
            )
        except Exception:
            path = f"configs/{agent.resource_type}/unknown.yaml"
        file_lines = f"  - `{path}`"
        resource_count = 1

    msg = (
        f"**Final PR Summary:**\n\n"
        f"| | |\n"
        f"|---|---|\n"
        f"| **Fork** | {fork_line} |\n"
        f"| **Branch** | `{agent.pr_branch_name}` |\n"
        f"| **Target** | `{upstream}:{agent.pr_target_branch}` |\n"
        f"| **Title** | {agent.pr_title} |\n"
        f"| **Files** | {resource_count} file(s) |\n\n"
        f"Files:\n{file_lines}\n\n"
        f"Shall I go ahead and **create the PR**?\n"
        f"Type **yes** to create, or **cancel** to abort."
    )
    return build_response(
        msg, resource_type=agent.resource_type, resource_status="pr_setup"
    )


async def _handle_confirm(session, agent, user_message):
    msg_lower = user_message.strip().lower()
    confirm_words = {
        "yes", "confirm", "ok", "y", "go ahead", "create", "create pr",
        "proceed", "do it", "1", "create the pr", "go", "sure",
    }

    if msg_lower in confirm_words:
        if agent.resource_type == "batch":
            return await execute_batch_pr_creation(session, agent)
        return await execute_pr_creation(session, agent)

    if "back" in msg_lower or "change" in msg_lower or "edit" in msg_lower:
        agent.pr_sub_phase = "ask_title"
        return build_response(
            "Sure! What would you like to change?\n"
            "  - `title: New Title` - change the PR title\n"
            "  - `body: New Body` - change the PR description\n"
            "  - Say **confirm** when done",
            resource_type=agent.resource_type,
            resource_status="pr_setup",
        )

    return build_response(
        "Please type **yes** to create the PR, **cancel** to abort,"
        " or **back** to make changes.",
        resource_type=agent.resource_type,
        resource_status="pr_setup",
    )


