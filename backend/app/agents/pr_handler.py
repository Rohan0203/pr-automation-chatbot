async def handle_pr_setup(session, user_message):
    agent = session.current_agent
    msg_lower = user_message.strip().lower()

    logger.info("[PR] handle_pr_setup: sub_phase=%s, user_msg=%.80s",
                getattr(agent, 'pr_sub_phase', 'ask_branch'), msg_lower[:80])

    if _is_skip(msg_lower):
        return _handle_skip(session, agent)

    if _is_cancel(msg_lower):
        return _handle_skip(session, agent)

    if _wants_new_resource(msg_lower):
        return _handle_pause_for_new_resource(session, agent)

    sub = getattr(agent, "pr_sub_phase", "ask_branch")

    if sub == "ask_branch":
        return _handle_ask_branch(session, agent, user_message)
    elif sub == "ask_title":
        return await _handle_ask_title(session, agent, user_message)
    elif sub == "show_files":
        return _handle_show_files(session, agent, user_message)
    elif sub == "confirm":
        return await _handle_confirm(session, agent, user_message)
    else:
        return await _handle_llm_fallback(session, agent, user_message)


