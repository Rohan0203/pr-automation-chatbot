async def present_pr_setup(session, agent):
    batch = session.batch
    is_batch = agent.resource_type == "batch"

    if is_batch:
        from collections import Counter

        count = len(batch)
        type_counts = Counter(e["resource_type"].upper() for e in batch)
        type_summary = ", ".join(f"{v} {k}" for k, v in type_counts.items())
        first_intake = batch[0]["intake_id"]
        default_branch = f"config/batch-{first_intake}-{int(time.time())}"
        agent.pr_title = agent.pr_title or f"[CONFIG] Batch - {type_summary}"
        body_lines = [
            f"## Infrastructure Configuration Batch ({count} resources)\n"
        ]
        for i, entry in enumerate(batch, 1):
            body_lines.append(
                f"### {i}. {entry['resource_type'].upper()} - {entry['resource_name']}"
            )
            body_lines.append(f"**Intake ID:** {entry['intake_id']}")
            body_lines.append(f"```yaml\n{entry['yaml']}\n```\n")
        body_lines.append(
            "---\n_This PR was automatically generated by PR Chatbot._"
        )
        agent.pr_body = agent.pr_body or "\n".join(body_lines)
    else:
        fields = agent.collected_fields or {}
        intake_id = fields.get("intake_id", "unknown")
        default_branch = (
            f"config/{agent.resource_type}-{intake_id}-{int(time.time())}"
        )
        if not agent.pr_title:
            agent.pr_title = _generate_smart_title(agent)
        if not agent.pr_body:
            agent.pr_body = _generate_smart_body(agent)

    agent.pr_branch_name = default_branch

    try:
        fork_info = await check_fork_status(session.github_token)
        agent.fork_exists = fork_info.get("exists", False)
        agent.fork_full_name = fork_info.get("fork_full_name")
    except Exception:
        agent.fork_exists = False

    try:
        branches = await get_upstream_branches(session.github_token)
        agent.available_branches = branches
    except Exception:
        branches = [settings.github_upstream_branch]
        agent.available_branches = branches

    agent.pr_target_branch = settings.github_upstream_branch
    agent.pr_sub_phase = "ask_branch"
    agent.phase = "pr_setup"

    branch_list = "\n".join(f"  - `{b}`" for b in branches[:10])
    upstream = f"{settings.github_upstream_owner}/{settings.github_upstream_repo}"

    msg = (
        f"**PR Setup**\n\n"
        f"Which branch should this PR target on `{upstream}`?\n"
        f"Available branches:\n{branch_list}\n\n"
        f"Type a branch name, or just say **confirm** to use the default"
        f" (`{agent.pr_target_branch}`).\n"
        f"You can also say **skip pr** or **cancel**."
    )

    return build_response(
        msg, resource_type=agent.resource_type, resource_status="pr_setup"
    )


